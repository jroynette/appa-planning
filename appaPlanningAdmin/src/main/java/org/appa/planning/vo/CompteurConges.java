package org.appa.planning.vo;

public class CompteurConges {

	private float congesAposer;

	private float rttAposer;

	private float congesEnAttente;

	private float rttEnAttente;

	public float getCongesAposer() {
		return congesAposer;
	}

	public void setCongesAposer(float congesAposer) {
		this.congesAposer = congesAposer;
	}

	public float getRttAposer() {
		return rttAposer;
	}

	public void setRttAposer(float rttAposer) {
		this.rttAposer = rttAposer;
	}

	public float getCongesEnAttente() {
		return congesEnAttente;
	}

	public void setCongesEnAttente(float congesEnAttente) {
		this.congesEnAttente = congesEnAttente;
	}

	public float getRttEnAttente() {
		return rttEnAttente;
	}

	public void setRttEnAttente(float rttEnAttente) {
		this.rttEnAttente = rttEnAttente;
	}
}
